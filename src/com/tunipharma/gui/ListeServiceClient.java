private void initCategorie() {
        ServiceDAO serviceDAO = new ServiceDAO();
        if (CBcategorie.getSelectedIndex() != -1) {
            CBcategorie.removeAllItems();
        }
        List<String> listec = serviceDAO.getAllCategories();
       // System.out.println(listec);
        for (String c : listec) {
           // System.out.println(c);
           CBcategorie.addItem(c);
        }
       
        
    }
      public void intialiseJTable() {
        DefaultTableModel model = (DefaultTableModel) jtableService.getModel();
        List<Service> services = serviceDAO.DisplayAllService(etat);
        for (Service service : services) {
            model.addRow(new Object[]{service.getId(), service.getNom(), service.getCategorie()});
        }
        jtableService.setModel(model);
    }

